import { NextRequest, NextResponse } from "next/server";
import { ZodError } from "zod"; // Import ZodError for specific error handling
import bcrypt from "bcrypt";
import clientPromise from "@/lib/database/mongodb";
import { generateOTP } from "@/lib/otp/genarate-otp";
import { sendOTPEmail } from "@/lib/email/send-otp"; // Adjust path if necessary
import { Db } from "mongodb"; // Import ObjectId for _id type if needed for schema
import { User } from "@/models/userSchema"; // IMPORTANT: Import User type from your schema file
import { userSchema } from "@/models/userSchema";

// Define a type for the user document as it will be stored in MongoDB.
// This should ideally align with your Zod schema and database model.
// We are now importing 'User' from '@/models/userSchema', so this local definition is removed.
